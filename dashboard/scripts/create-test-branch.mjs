#!/usr/bin/env node
/**
 * Create a throwaway Neon branch for integration / Playwright runs.
 *
 * Usage:
 *   node dashboard/scripts/create-test-branch.mjs [--name <branch>] [--project-id <id>] [--parent <branch>] [--keep]
 *
 * Reads HARNESS_NEON_PROJECT_ID from the environment if --project-id is
 * omitted (defaults to the harness-lab project). Writes the resulting
 * pooled connection string into dashboard/.env.test.local under
 * HARNESS_TEST_DATABASE_URL so the test runner can pick it up.
 *
 * `--keep` skips writing .env.test.local — useful for ad-hoc spelunking.
 */

import { spawn } from "node:child_process";
import { writeFile, mkdir } from "node:fs/promises";
import path from "node:path";

const DEFAULT_PROJECT_ID = process.env.HARNESS_NEON_PROJECT_ID ?? "broad-smoke-45468927";
const DEFAULT_PARENT = "main";

function parseArgs(argv) {
  const args = { keep: false };
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    switch (token) {
      case "--name":
        args.name = argv[++index];
        break;
      case "--project-id":
        args.projectId = argv[++index];
        break;
      case "--parent":
        args.parent = argv[++index];
        break;
      case "--keep":
        args.keep = true;
        break;
      case "--help":
      case "-h":
        printUsageAndExit(0);
        break;
      default:
        console.error(`unknown argument: ${token}`);
        printUsageAndExit(1);
    }
  }
  return args;
}

function printUsageAndExit(code) {
  console.error(
    "usage: create-test-branch.mjs [--name <branch>] [--project-id <id>] [--parent <branch>] [--keep]",
  );
  process.exit(code);
}

function runNeonctl(args) {
  return new Promise((resolve, reject) => {
    const child = spawn("neonctl", args, { stdio: ["ignore", "pipe", "pipe"] });
    const stdout = [];
    const stderr = [];
    child.stdout.on("data", (chunk) => stdout.push(chunk));
    child.stderr.on("data", (chunk) => stderr.push(chunk));
    child.on("error", reject);
    child.on("close", (code) => {
      if (code !== 0) {
        reject(
          new Error(
            `neonctl ${args.join(" ")} exited with code ${code}\n${Buffer.concat(stderr).toString("utf8")}`,
          ),
        );
        return;
      }
      resolve(Buffer.concat(stdout).toString("utf8"));
    });
  });
}

async function createBranch({ projectId, parent, name }) {
  const branchName = name ?? `test-${Date.now()}`;
  const raw = await runNeonctl([
    "branches",
    "create",
    "--project-id",
    projectId,
    "--parent",
    parent,
    "--name",
    branchName,
    "--output",
    "json",
  ]);
  const parsed = JSON.parse(raw);
  return { branchName, branch: parsed.branch ?? parsed };
}

async function fetchConnectionString({ projectId, branchName }) {
  const raw = await runNeonctl([
    "connection-string",
    branchName,
    "--project-id",
    projectId,
    "--pooled",
  ]);
  return raw.trim();
}

async function writeEnv({ branchName, branch, connectionString }) {
  const envPath = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..", ".env.test.local");
  await mkdir(path.dirname(envPath), { recursive: true });
  const lines = [
    `# Auto-generated by create-test-branch.mjs at ${new Date().toISOString()}`,
    `# Branch: ${branchName} (${branch.id ?? "unknown id"})`,
    `# Delete with: node dashboard/scripts/delete-test-branch.mjs`,
    `HARNESS_TEST_BRANCH_NAME=${branchName}`,
    `HARNESS_TEST_BRANCH_ID=${branch.id ?? ""}`,
    `HARNESS_TEST_DATABASE_URL=${connectionString}`,
    "",
  ];
  await writeFile(envPath, lines.join("\n"), "utf8");
  return envPath;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const projectId = args.projectId ?? DEFAULT_PROJECT_ID;
  const parent = args.parent ?? DEFAULT_PARENT;

  const { branchName, branch } = await createBranch({ projectId, parent, name: args.name });
  const connectionString = await fetchConnectionString({ projectId, branchName });

  if (args.keep) {
    console.log(`branch ${branchName} created (${branch.id ?? "unknown id"})`);
    console.log(connectionString);
    return;
  }

  const envPath = await writeEnv({ branchName, branch, connectionString });
  console.log(`branch ${branchName} created and connection string written to ${envPath}`);
}

main().catch((error) => {
  console.error(error.message ?? error);
  process.exit(1);
});
